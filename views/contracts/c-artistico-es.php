    <h1 id="main-title">Addendum Proveedor Artístico <br>
    Oferta de servicio de cameo</h1>

    <p>El PROVEEDOR ARTÍSTICO es la persona/compañía que representa al artista o el artista directamente autorepresentado para el manejo de su imagen y actuación personal en videos, películas, series comúnmente llamado “cameo”.</p>

    <h2>Objeto</h2>

    <p>Se exhibe el perfil del artista o personaje público de alto reconocimiento, ofreciendo la actuación especial o hacer su aparición en una película, programa de TV, serie, videojuego, comercial u otra producción audiovisual.</p>

    <p>También se ofrece la elaboración de canciones del artista, en caso de poder hacerlo a solicitud especial de un comprador.</p>

    <h2>Servicio</h2>

    <p>Se realiza la activación de este servicio por un valor de suscripción más la comisión del contrato realizado.</p>

    <p>Durante este año se exhibe la imagen y se pone a disposición para proyectos audiovisuales, a quienes se debe dar respuesta oportuna confirmando previamente si el artista o personaje de alto reconocimiento, puede dar cumplimiento con los tiempos de grabación y está de acuerdo con todo lo necesario para que se dé la grabación de la actuación especial, cameo o canción bajo demanda.</p>

    <h2>Estipulaciones del contrato</h2>

    <p>Se realizará el contrato personalizado a solicitud del comprador y si es aprobado el artista garantiza su actuación personal y cumplimiento con el mismo.</p>

    <p>FILMTONO media para lograr el pacto y comisiona por el acuerdo realizado y pagado.</p>

    <h2>Pago por contrato</h2>

    <p>Para estos servicios de cameo o solicitudes especiales, se solicita el pago del 80 antes de la ejecución y 20% al finalizar la ejecución como garantía de cumplimiento.</p>

