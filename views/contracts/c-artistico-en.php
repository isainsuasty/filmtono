    <h1 id="main-title">Artistic Supplier Addendum <br>
    Cameo service offer</h1>

    <p>The ARTISTIC PROVIDER is the person/company that represents the artist or the artist directly self-represented for the management of his image and personal performance in videos, movies, series commonly called “cameo”.</p>

    <h2>Purpose</h2>

    <p>The profile of the highly acclaimed artist or public figure is displayed, offering the special performance or making an appearance in a film, TV show, series, video games, commercial or other audiovisual production.</p>

    <p>También se ofrece la elaboración de canciones del artista, en caso de poder hacerlo a solicitud especial de un comprador.</p>

    <h2>Service</h2>

    <p>The activation of this service is carried out for a subscription value plus the commission of the contract made.</p>

    <p>During this year the image is exhibited and made available for audiovisual projects, to which a timely response must be given confirming in advance whether the artist or character of high recognition, can comply with the recording times and agrees with everything necessary for the recording of the special performance, cameo or song on demand.</p>

    <h2>Terms of the contract</h2>

    <p>The personalized contract will be made at the request of the buyer and if approved the artist guarantees his personal performance and compliance with it.</p>

    <p>FILMTONO averages to achieve the pact and commissions for the agreement made and paid.</p>

    <h2>Payment by contract</h2>

    <p>For these cameo services or special requests, the payment of 80 before the performance and 20% at the end of the performance is requested as a guarantee of performance.</p>

